#!/bin/bash

# RunPod Container-Friendly Setup Script
# This version works without systemd in container environments

set -e

echo "üöÄ Starting RunPod container deployment setup..."

# =============================================================================
# CONFIGURATION
# =============================================================================

APP_NAME="task-management-system"
LLM_MODEL=${LLM_MODEL:-"llama3.1:8b"}
BACKUP_MODEL=${BACKUP_MODEL:-"qwen2.5:7b"}

# =============================================================================
# ENVIRONMENT CHECK
# =============================================================================

echo "üîç Checking environment..."

# Check if we're in the right directory
if [ ! -f ".env.production" ]; then
    echo "‚ùå .env.production not found. Please make sure you're in the IMPORTANT directory and have configured your environment."
    exit 1
fi

echo "‚úÖ Environment file found"

# =============================================================================
# DOCKER SETUP (Container-friendly)
# =============================================================================

echo "üê≥ Setting up Docker services..."

# Start Docker daemon if not running
if ! docker info >/dev/null 2>&1; then
    echo "üöÄ Starting Docker daemon..."
    dockerd > /var/log/docker.log 2>&1 &
    sleep 10
    
    # Wait for Docker to be ready
    timeout=60
    counter=0
    while ! docker info >/dev/null 2>&1; do
        if [ $counter -gt $timeout ]; then
            echo "‚ùå Timeout waiting for Docker to start"
            exit 1
        fi
        echo "Waiting for Docker... ($counter/$timeout)"
        sleep 2
        counter=$((counter + 2))
    done
fi

echo "‚úÖ Docker is running"

# =============================================================================
# OLLAMA SETUP
# =============================================================================

echo "ü§ñ Setting up Ollama LLM service..."

# Pull and start Ollama
echo "üì• Starting Ollama container..."
docker run -d \
    --name ollama \
    --gpus all \
    -p 11434:11434 \
    -v ollama_data:/root/.ollama \
    -e OLLAMA_HOST=0.0.0.0 \
    -e OLLAMA_ORIGINS=* \
    --restart unless-stopped \
    ollama/ollama

# Wait for Ollama to be ready
echo "‚è≥ Waiting for Ollama to start..."
timeout=120
counter=0
while ! curl -s http://localhost:11434/api/tags > /dev/null; do
    if [ $counter -gt $timeout ]; then
        echo "‚ùå Timeout waiting for Ollama to start"
        docker logs ollama
        exit 1
    fi
    echo "Waiting for Ollama... ($counter/$timeout)"
    sleep 3
    counter=$((counter + 3))
done

echo "‚úÖ Ollama is ready"

# Pull LLM models
echo "üì• Downloading AI models (this will take several minutes)..."
echo "Downloading $LLM_MODEL..."
docker exec ollama ollama pull $LLM_MODEL || {
    echo "‚ö†Ô∏è Primary model download failed, trying alternative..."
    docker exec ollama ollama pull llama3.1:latest
}

echo "Downloading $BACKUP_MODEL..."
docker exec ollama ollama pull $BACKUP_MODEL || {
    echo "‚ö†Ô∏è Backup model download failed, trying alternative..."
    docker exec ollama ollama pull qwen2.5:latest
}

echo "‚úÖ AI models downloaded"

# Test Ollama
echo "üß™ Testing Ollama API..."
curl -s http://localhost:11434/api/tags || {
    echo "‚ùå Ollama API test failed"
    docker logs ollama
    exit 1
}

echo "‚úÖ Ollama API is working"

# =============================================================================
# APPLICATION BUILD
# =============================================================================

echo "üèóÔ∏è Building Next.js application..."

# Check if we have Node.js for build (if not, use Docker)
if ! command -v node &> /dev/null; then
    echo "üì¶ Building application with Docker..."
    
    # Build the application image
    docker build -f docker/Dockerfile.production -t task-management-app .
    
    echo "‚úÖ Application built successfully"
else
    echo "üì¶ Building application with local Node.js..."
    
    # Install dependencies and build
    npm install
    npm run build
    
    echo "‚úÖ Application built successfully"
fi

# =============================================================================
# APPLICATION DEPLOYMENT
# =============================================================================

echo "üöÄ Deploying application..."

# Source environment variables
source .env.production

# Create docker-compose.yml for production
cat > docker-compose.production.yml << EOF
version: '3.8'

services:
  # Next.js Application
  task_management_app:
    image: task-management-app
    container_name: task_management_web
    ports:
      - "3000:3000"
    environment:
      # Database Connection
      - NEXT_PUBLIC_SUPABASE_URL=$SUPABASE_URL
      - NEXT_PUBLIC_SUPABASE_ANON_KEY=$SUPABASE_ANON_KEY
      - SUPABASE_SERVICE_ROLE_KEY=$SUPABASE_SERVICE_ROLE_KEY
      
      # LLM Configuration
      - LLM_ENDPOINT=http://host.docker.internal:11434
      - LLM_MODEL=$LLM_MODEL
      - LLM_PROVIDER=ollama
      
      # Application Settings
      - NODE_ENV=production
      - NEXTAUTH_SECRET=\${NEXTAUTH_SECRET:-$(openssl rand -base64 32)}
      - NEXTAUTH_URL=http://localhost:3000
      
      # Security
      - CORS_ORIGIN=http://localhost:3000
      - API_SECRET_KEY=\${API_SECRET_KEY:-$(openssl rand -base64 32)}
      
    restart: unless-stopped
    extra_hosts:
      - "host.docker.internal:host-gateway"
    healthcheck:
      test: ["CMD", "curl", "-f", "http://localhost:3000/api/health"]
      interval: 30s
      timeout: 10s
      retries: 3
      start_period: 30s

networks:
  default:
    driver: bridge
EOF

# Start the application
echo "üåê Starting application container..."
docker-compose -f docker-compose.production.yml up -d

# Wait for application to be ready
echo "‚è≥ Waiting for application to start..."
timeout=60
counter=0
while ! curl -s http://localhost:3000/api/health > /dev/null; do
    if [ $counter -gt $timeout ]; then
        echo "‚ùå Timeout waiting for application to start"
        docker-compose -f docker-compose.production.yml logs
        exit 1
    fi
    echo "Waiting for application... ($counter/$timeout)"
    sleep 3
    counter=$((counter + 3))
done

echo "‚úÖ Application is running"

# =============================================================================
# NGINX CONFIGURATION (Simple)
# =============================================================================

echo "üîß Setting up Nginx reverse proxy..."

# Create simple nginx config
cat > /etc/nginx/sites-available/task-management << 'EOF'
server {
    listen 80 default_server;
    listen [::]:80 default_server;
    
    server_name _;
    client_max_body_size 100M;

    # Security headers
    add_header X-Frame-Options "SAMEORIGIN" always;
    add_header X-XSS-Protection "1; mode=block" always;
    add_header X-Content-Type-Options "nosniff" always;

    # Main application
    location / {
        proxy_pass http://localhost:3000;
        proxy_http_version 1.1;
        proxy_set_header Upgrade $http_upgrade;
        proxy_set_header Connection 'upgrade';
        proxy_set_header Host $host;
        proxy_set_header X-Real-IP $remote_addr;
        proxy_set_header X-Forwarded-For $proxy_add_x_forwarded_for;
        proxy_set_header X-Forwarded-Proto $scheme;
        proxy_cache_bypass $http_upgrade;
        proxy_read_timeout 300;
        proxy_connect_timeout 300;
        proxy_send_timeout 300;
    }

    # LLM API endpoint
    location /api/llm/ {
        proxy_pass http://localhost:11434/;
        proxy_http_version 1.1;
        proxy_set_header Host $host;
        proxy_set_header X-Real-IP $remote_addr;
        proxy_set_header X-Forwarded-For $proxy_add_x_forwarded_for;
        proxy_read_timeout 600;
        proxy_connect_timeout 600;
        proxy_send_timeout 600;
    }

    # Health check endpoint
    location /health {
        proxy_pass http://localhost:3000/api/health;
        access_log off;
    }
}
EOF

# Enable the site
rm -f /etc/nginx/sites-enabled/default
ln -sf /etc/nginx/sites-available/task-management /etc/nginx/sites-enabled/

# Test nginx config
nginx -t || {
    echo "‚ùå Nginx configuration test failed"
    exit 1
}

# Start nginx
nginx || {
    echo "‚ö†Ô∏è Starting nginx in background..."
    nginx -g "daemon off;" &
}

echo "‚úÖ Nginx configured and running"

# =============================================================================
# MONITORING SCRIPTS
# =============================================================================

echo "üìä Setting up monitoring..."

# Create health check script
cat > /usr/local/bin/health-check.sh << 'EOF'
#!/bin/bash

echo "üîç System Health Check - $(date)"
echo "=================================="

# Check Docker
if docker info >/dev/null 2>&1; then
    echo "‚úÖ Docker is running"
else
    echo "‚ùå Docker is not running"
fi

# Check containers
echo ""
echo "üì¶ Container Status:"
docker ps --format "table {{.Names}}\t{{.Status}}\t{{.Ports}}"

# Check services
echo ""
echo "üåê Service Health:"
if curl -s -f "http://localhost:3000/api/health" > /dev/null; then
    echo "‚úÖ Next.js Application is healthy"
else
    echo "‚ùå Next.js Application is unhealthy"
fi

if curl -s -f "http://localhost:11434/api/tags" > /dev/null; then
    echo "‚úÖ LLM Service (Ollama) is healthy"
else
    echo "‚ùå LLM Service (Ollama) is unhealthy"
fi

# Check GPU (if available)
if command -v nvidia-smi &> /dev/null; then
    echo ""
    echo "üñ•Ô∏è GPU Status:"
    nvidia-smi --query-gpu=name,utilization.gpu,memory.used,memory.total --format=csv,noheader,nounits
fi

echo ""
echo "‚úÖ Health check completed"
EOF

chmod +x /usr/local/bin/health-check.sh

echo "‚úÖ Monitoring setup completed"

# =============================================================================
# COMPLETION
# =============================================================================

echo ""
echo "üéâ RunPod container deployment completed successfully!"
echo ""
echo "üìã Service Status:"
echo "=================="
docker ps

echo ""
echo "üåê Access URLs:"
echo "==============="
echo "Application: http://localhost:3000"
echo "LLM API: http://localhost:11434"
echo "Health Check: http://localhost:3000/api/health"

echo ""
echo "üîß Management Commands:"
echo "======================"
echo "Health Check: /usr/local/bin/health-check.sh"
echo "View App Logs: docker logs task_management_web"
echo "View LLM Logs: docker logs ollama"
echo "Restart App: docker-compose -f docker-compose.production.yml restart"

echo ""
echo "üß™ Quick Test:"
echo "============="
/usr/local/bin/health-check.sh

echo ""
echo "‚úÖ Your task management system with local LLM is ready!"
echo "üöÄ Access your application at the RunPod public URL (port 3000)" 